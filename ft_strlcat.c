#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t dstsize)
{
	size_t	i;
	size_t	j;
	size_t	len_dst;
	size_t	len_src;

	len_dst = ft_strlen(dst);
	len_src = ft_strlen(src);
	if (!dst)
		return (len_src);
	j = len_dst;
	i = 0;
	if (dstsize == 0 || dstsize < len_dst)
		return (len_src + dstsize);
	while (j < dstsize - 1 && src[i] != '\0')
	{
		dst[j] = src[i];
		j++;
		i++;
	}
	dst[j] = '\0';
	return (len_dst + len_src);
}
